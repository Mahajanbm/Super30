package com.demo.operaters;

public class Demo {

	public static void main(String[] args) {
		 int i=1;
		++i;
		 System.out.println(i--);//2
		 System.out.println(i++);//1
		 System.out.println(--i);//1
			
		 System.out.println(++i);//2
		 System.out.println(i--);//2
		 System.out.println(--i);//0
	}

}
